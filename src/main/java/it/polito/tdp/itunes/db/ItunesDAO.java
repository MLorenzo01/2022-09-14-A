	public List<Album> getAllAlbums(){
		final String sql = "SELECT * FROM Album";
		List<Album> result = new LinkedList<>();
		
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			ResultSet res = st.executeQuery();

			while (res.next()) {
				result.add(new Album(res.getInt("AlbumId"), res.getString("Title"), 0.0));
			}
			conn.close();
		} catch (SQLException e) {
			e.printStackTrace();
			throw new RuntimeException("SQL Error");
		}
		return result;
	}
	
	public List<Album> getAlbums(int durata){
		this.idMap = new TreeMap<>();
		final String sql = "SELECT *, SUM(t.Milliseconds) AS somma "
				+ "FROM album a, track t "
				+ "WHERE a.AlbumId = t.AlbumId "
				+ "GROUP BY a.AlbumId "
				+ "HAVING SUM(t.Milliseconds) > ?";
		List<Album> result = new LinkedList<>();
		
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			st.setInt(1, durata*60*1000);
			ResultSet res = st.executeQuery();

			while (res.next()) {
				Album a = new Album(res.getInt("AlbumId"), res.getString("Title"), res.getDouble("somma"));
				result.add(a);
				idMap.put(a.getAlbumId(), a);
			}
			conn.close();
		} catch (SQLException e) {
			e.printStackTrace();
			throw new RuntimeException("SQL Error");
		}
		return result;
	}
	public ArrayList<Edge> getCoppie(){
		final String sql = "SELECT DISTINCTROW a1.AlbumId as a1, a2.AlbumId as a2 "
				+ "FROM album a1, track t1, album a2, track t2, playlisttrack pt, playlisttrack pt2 "
				+ "WHERE a1.AlbumId = t1.AlbumId AND a2.AlbumId = t2.AlbumId AND pt.TrackId = t1.TrackId AND pt2.TrackId = t2.TrackId AND pt.PlaylistId = pt2.PlaylistId "
				+ "AND a1.AlbumId < a2.AlbumId";
		ArrayList<Edge> result = new ArrayList<>();
		
		try {
			Connection conn = DBConnect.getConnection();
			PreparedStatement st = conn.prepareStatement(sql);
			ResultSet res = st.executeQuery();

			while (res.next()) {
				if(idMap.get(res.getInt("a1")) != null && idMap.get(res.getInt("a2")) != null)
					result.add(new Edge(idMap.get(res.getInt("a1")), idMap.get(res.getInt("a2"))));
			}
			conn.close();
		} catch (SQLException e) {
			e.printStackTrace();
			throw new RuntimeException("SQL Error");
		}
		return result;
	}
	
